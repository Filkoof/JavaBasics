public class Country {

    private String countryName;
    private long countPopulation;
    private int area;
    private String capitalName;
    private String seaExit;

    public String getCountryName() {
        return countryName;
    }

    public void setCountryName(String countryName) {
        this.countryName = countryName;
    }

    public long getCountPopulation() {
        return countPopulation;
    }

    public void setCountPopulation(int countPopulation) {
        this.countPopulation = countPopulation;
    }

    public int getArea() {
        return area;
    }

    public void setArea(int area) {
        this.area = area;
    }

    public String getCapitalName() {
        return capitalName;
    }

    public void setCapitalName(String capitalName) {
        this.capitalName = capitalName;
    }

    public String getSeaExit() {
        return seaExit;
    }

    public void setSeaExit(String seaExit) {
        this.seaExit = seaExit;
    }
}